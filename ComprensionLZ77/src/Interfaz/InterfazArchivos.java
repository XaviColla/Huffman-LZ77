    private void BotonVerArchivoCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonVerArchivoCActionPerformed
        // TODO add your handling code here:
        
        if (Desktop.isDesktopSupported()) {
            try {
                if(this.CampoArchivo.getText().contains(".lz77"))
                {
                    File myFile = new File(this.CampoArchivo.getText());
                    Desktop.getDesktop().open(myFile);
                }
                else
                {
                    File myFile = new File(this.CampoArchivo.getText() + ".lz77");
                    Desktop.getDesktop().open(myFile);
                }
                
            } catch (IOException ex) {
                // no application registered for PDFs
            }
         }
    }//GEN-LAST:event_BotonVerArchivoCActionPerformed

    private void BotonComp1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonComp1ActionPerformed
        // TODO add your handling code here:}
        long time_start = 0;
        long time_end = 0;
        long timeT = 0;
        LZ77 nuevaCompre = new LZ77();
        try
        {
            if(!CampoArchivo.getText().contains(".lz77"))
                time_start = System.currentTimeMillis();
                nuevaCompre.comprimir(CampoArchivo.getText());
                time_end = System.currentTimeMillis();
                timeT = (time_end - time_start);
                this.labeltiempocomprension.setText("Tiempo: " + String.valueOf(timeT) + " Segundos");
                File arch = new File(this.CampoArchivo.getText()+ ".lz77");
                this.ValorTxtCom.setText("" + arch.length() + " bytes");
                BotonVerArchivoC.setEnabled(true);
        }
        catch(Exception io){
            
        }
        
    }//GEN-LAST:event_BotonComp1ActionPerformed

