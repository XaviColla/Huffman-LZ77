    private void BotonBuscarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscarArchivoActionPerformed
        // TODO add your handling code here:
        JFileChooser ventanaBusqueda = new JFileChooser();//Objeto ventana de busqueda
         ventanaBusqueda.setFileSelectionMode(JFileChooser.FILES_ONLY);
         //FileNameExtensionFilter  filter = new FileNameExtensionFilter("Archivo Txt", "txt");
         //ventanaBusqueda.setFileFilter(filter);
         int returnVal = ventanaBusqueda.showOpenDialog(null);//Condicional de aceptacion seleccion
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File archivo = ventanaBusqueda.getSelectedFile();
            CampoArchivo.setText(archivo.getPath());
            this.CampoArchivo.setText(archivo.getAbsolutePath());

           
                File arch = new File(this.CampoArchivo.getText());
                this.NombreArchivo.setText(arch.getName());
                this.ValorTxtInicial.setText("" + arch.length() + " bytes");
        }
    }//GEN-LAST:event_BotonBuscarArchivoActionPerformed

